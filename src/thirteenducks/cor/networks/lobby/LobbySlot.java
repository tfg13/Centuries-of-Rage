    public void changeteam(int newteam) {
        teambox.getModel().setSelectedItem(String.valueOf(newteam));
        this.repaint();
    }

    public void changeReady(boolean ready) {
        if (ready == true) {
            this.jLabel1.setForeground(Color.green);
        } else {
            this.jLabel1.setForeground(Color.black);
        }

    }

    public void setHost() {

        //this.jLabel1.setBackground(Color.yellow);
        this.jLabel2.setVisible(true);
    }

    public void changerace(int newrace) {
        raceboxactive = false;
        if (newrace == races.undead) {
            racebox.getModel().setSelectedItem("undead");
        } else if (newrace == races.human) {
            racebox.getModel().setSelectedItem("human");
        } else if (newrace == races.random) {
            racebox.getModel().setSelectedItem("random");
        }
        this.repaint();
        raceboxactive = true;
    }

    public void changecolour(String newcolour) {

        color = new java.awt.Color(Integer.parseInt(newcolour));
        jButton2.setBackground(color);

        //colourbox.getModel().setSelectedItem(colour);    // team1 ist das erste item, team2 das zweite usw. es lebe der pfusch
        this.repaint();
    }

