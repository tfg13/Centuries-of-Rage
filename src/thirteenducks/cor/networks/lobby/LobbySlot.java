    private void raceboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_raceboxActionPerformed
        // TODO add your handling code here:
        if (raceboxactive == true) {
            if (racebox.getModel().getSelectedItem().equals("undead")) {
                lobby.send('9' + "1" + name);
            } else if (racebox.getModel().getSelectedItem().equals("human")) {
                lobby.send('9' + "2" + name);
            } else if (racebox.getModel().getSelectedItem().equals("random")) {
                lobby.send('9' + "3" + name);
            }
        }
    }//GEN-LAST:event_raceboxActionPerformed

